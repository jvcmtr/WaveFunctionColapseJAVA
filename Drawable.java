public class Drawable{
  public String c = " ";
  public String allowedLeft = " ";
  public String allowedRight = " ";
  public String allowedUp = " ";
  public String allowedBottom = " ";

  public Drawable(String C , String left, String right, String up, String bottom ){
    c = C;
    allowedLeft = left;
    allowedRight = right;
    allowedUp = up;
    allowedBottom = bottom;
  }

  public Drawable(){
  }
}